const fs = require('fs');
const path = require('path');

const rootDir = path.resolve(__dirname, '..');
const envPath = path.join(rootDir, '.env');
const outputPath = path.join(rootDir, 'src', 'config', 'env.js');

const readEnv = () => {
  if (!fs.existsSync(envPath)) {
    return {};
  }

  return fs
    .readFileSync(envPath, 'utf8')
    .split(/\r?\n/)
    .reduce((values, line) => {
      const trimmedLine = line.trim();

      if (!trimmedLine || trimmedLine.startsWith('#')) {
        return values;
      }

      const separatorIndex = trimmedLine.indexOf('=');

      if (separatorIndex === -1) {
        return values;
      }

      const key = trimmedLine.slice(0, separatorIndex).trim();
      const value = trimmedLine.slice(separatorIndex + 1).trim().replace(/^['"]|['"]$/g, '');

      values[key] = value;
      return values;
    }, {});
};

const env = readEnv();
const groqApiKey = env.GROQ_API_KEY || env.API_KEY || 'your_groq_api_key_here';
const fileContents = `// This file is generated by scripts/generate-env-config.js.
// Do not edit it directly. Update .env instead.
export const GROQ_API_KEY = ${JSON.stringify(groqApiKey)};
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, fileContents);

console.log('Generated src/config/env.js from .env');
